    /**
     * Definition for a binary tree node.
     * public class TreeNode {
     * int val;
     * TreeNode left;
     * TreeNode right;
     * TreeNode(int x) { val = x; }
     * }
     */

    public static class TreeNode {
        int val;
        TreeNode left;
        TreeNode right;

        TreeNode(int x) {
            val = x;
        }
    }

    public class Codec {

        // Encodes a tree to a single string.
        public String serialize(TreeNode root) {
            //å±‚åºéå†
            if (root == null) {
                return "";
            }
            StringBuilder res = new StringBuilder();
            Queue<TreeNode> queue = new LinkedList<>();
            queue.offer(root);
            while (!queue.isEmpty()) {

                TreeNode node = queue.poll();

                if (node != null) {
                    res.append(node.val);
                    queue.offer(node.left);
                    queue.offer(node.right);
                } else {
                    res.append("null");
                }
                res.append(",");
            }
            res.deleteCharAt(res.length() - 1);
            return res.toString();
        }

        // Decodes your encoded data to tree.
        public TreeNode deserialize(String data) {

            if (data.length() == 0 || data.charAt(0) == 'n') {
                return null;
            }
            String[] chars = data.split(",");

            TreeNode head = new TreeNode(Integer.parseInt(chars[0]));
            Queue<TreeNode> queue = new LinkedList<>();
            queue.offer(head);

            int i = 1;
            while (!queue.isEmpty()) {

                TreeNode node = queue.poll();

                if (i < chars.length && !chars[i].equals("null")) {
                    node.left = new TreeNode(Integer.parseInt(chars[i]));
                    queue.offer(node.left);
                }
                ++i;
                if (i < chars.length && !chars[i].equals("null")) {
                    node.right = new TreeNode(Integer.parseInt(chars[i]));
                    queue.offer(node.right);
                }
                ++i;
            }
            return head;
        }
    }

